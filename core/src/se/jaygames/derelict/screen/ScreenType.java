package se.jaygames.derelict.screen;

public enum ScreenType {

	/** Indicates that we want to go to the splash screen */
	SPLASH_SCREEN,
	/** Indicates that we want to go to level select screen */
	LEVEL_SELECT,
	/** Indicates that we want to go to the my cow screen */
	MY_COW,
	/** Indicates that we want to go to the game screen. */
	GAME_SCREEN,
	/** Indicates that we want to go to the credits screen. */
	CREDITS;
	
}
